			<h3>Step 1 - Install & Activate Plugin<h3>
			<p>After downloaded plugin from WordPress.</p>
			<p>Login to your WordPress site upload the plugin, install and activate.</p>
			
			<h3>Step 2 - Create A Headline<h3>
			<p>Plugin has been installed on site.</p>
			<p>Now, go to the plugin's menu "Floating Headline" and click on "Add Floating Headline" page.</p>
			<p>Set headline setting like Headline Text, Text-Style( Size, Text-Color, Background-Color ) , Link, Close-Button(Hide/Show), Rotation-Speed, Repeatation .</p>
			<p>Finally click on "Publish" button to create headline.</p>
			
			<h3>Step 3 - Display Headline On Site<h3>
			<p>Our headline shortcode has been created in last step.</p>
			<p>Copy the heighlighted shortcode above the headline setting.
			<p>Now create a new Page / Post and paste the copied headline shortcode into content part.</p>
			<p>Publish the Page / Post and view the page for headline display.</p>
			<pre>[FHS id=4]</pre>
			<p>Here id = 4 is your headline post id.</p>
